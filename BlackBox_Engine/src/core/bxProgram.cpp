void bxProgram::init()
{
	this->renderContext = new bxContext(670, 670, " ", false);
	this->shader = new Shader();
	BBX_INFO("Loading shaders");
	shader->loadShader();
	BBX_INFO("Shaders loaded.");

	GLuint shaderID = shader->getID();
	this->renderContext->setShader(shaderID);
}

